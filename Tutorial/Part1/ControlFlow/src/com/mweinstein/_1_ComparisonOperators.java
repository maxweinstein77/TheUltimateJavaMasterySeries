package com.mweinstein;

public class _1_ComparisonOperators {

    public static void main(String[] args) {
        int x1 = 1;
        int y1 = 1;
        System.out.println(x1 == y1);
        System.out.println(x1 >= y1);
        System.out.println(x1 <= y1);

        int x2 = 1;
        int y2 = 2;
        System.out.println(x2 == y2);
        System.out.println(x2 >= y2);
        System.out.println(x2 <= y2);

    }
}
