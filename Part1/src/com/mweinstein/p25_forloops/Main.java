package com.mweinstein.p25_forloops;

public class Main {

   public static void main(String[] args) {

      // Ascending order
      for (int i = 0; i < 5; i++)
         System.out.println("Hello World " + i);

      // Reverse order
      for (int i = 5; i > 0; i--)
         System.out.println("Hello World " + i);
   }
}


